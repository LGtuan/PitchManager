package com.example.duan1_pro1121.model.statistical;

public class DoanhThu {
    private String name;
    private int money;

    public DoanhThu(String name, int money) {
        this.name = name;
        this.money = money;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }
}
